package PKG1;

public class MinimumNum {

	public static void main(String[] args) {

		int a [] = {54 , 32 , 45 , 65 , 4};
		int  miniNum =a[0];
		for (int i =0;   i<a.length;   i++)
		{
			if(miniNum>a[i])
			{
				miniNum=a[i];
			}
		}
		System.out.println("Minimnum number is "      +   miniNum);
			
			
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
